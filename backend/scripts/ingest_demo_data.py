# Bryan / College Station, TX area (approximate center)
BRYAN_CS_LON = -96.33
BRYAN_CS_LAT = 30.63

DEMO_NOTE = (
    "SYNTHETIC DEMO DATA — NOT REAL. This record was generated by "
    "scripts/ingest_demo_data.py for development and testing purposes only. "
    "It does not represent authoritative wetland, floodplain, or transmission "
    "line information."
)


def make_parcel_polygon(
    center_lon: float,
    center_lat: float,
    width_meters: float,
    height_meters: float,
    rotation_deg: float = 0.0,
) -> Polygon:
    """Create a rectangular parcel polygon centered at the given WGS84 point.

    Width/height are in metres in the analysis CRS; the polygon is generated
    in the analysis CRS and then converted to WGS84 for display.
    """
    import math

    from shapely.geometry import box as shapely_box
    from shapely.ops import transform

    from app.geometry.crs import get_transformer

    settings = get_settings()
    # Transform center from WGS84 to analysis CRS
    transformer = get_transformer(settings.WGS84_CRS, settings.ANALYSIS_CRS)
    cx, cy = transformer.transform(center_lon, center_lat)

    half_w = width_meters / 2
    half_h = height_meters / 2
    rect = shapely_box(cx - half_w, cy - half_h, cx + half_w, cy + half_h)

    if rotation_deg:
        angle = math.radians(rotation_deg)
        cos_a = math.cos(angle)
        sin_a = math.sin(angle)

        def rotate(x, y):
            dx = x - cx
            dy = y - cy
            return (cx + dx * cos_a - dy * sin_a, cy + dx * sin_a + dy * cos_a)

        rect = transform(rotate, rect)

    return rect


def make_constraint_polygon(
    center_lon: float,
    center_lat: float,
    width_meters: float,
    height_meters: float,
) -> Polygon:
    """Create a rectangular constraint polygon in the analysis CRS."""
    settings = get_settings()
    from shapely.geometry import box as shapely_box

    from app.geometry.crs import get_transformer

    transformer = get_transformer(settings.WGS84_CRS, settings.ANALYSIS_CRS)
    cx, cy = transformer.transform(center_lon, center_lat)

    half_w = width_meters / 2
    half_h = height_meters / 2
    return shapely_box(cx - half_w, cy - half_h, cx + half_w, cy + half_h)


def to_wkt_with_srid(geom, srid: int) -> str:
    """Return a WKT string with SRID prefix for ST_GeomFromEWKT."""
    return f"SRID={srid};{geom.wkt}"


def ingest_parcels(session: Session) -> list[Parcel]:
    """Create 5 synthetic demo parcels around Bryan/College Station, TX."""
    parcels_data = [
        # (display_name, source_id, lon_offset, lat_offset, width_m, height_m, rotation, address)
        (
            "Demo Parcel A - Brazos County",
            "DEMO-PARCEL-A",
            -0.005,
            0.003,
            450,
            450,
            0,
            "Demo Area A, Brazos County, TX",
        ),
        (
            "Demo Parcel B - Near Navasota River",
            "DEMO-PARCEL-B",
            0.010,
            -0.004,
            700,
            740,
            15,
            "Demo Area B, Brazos County, TX",
        ),
        (
            "Demo Parcel C - College Station Area",
            "DEMO-PARCEL-C",
            -0.002,
            -0.006,
            240,
            250,
            0,
            "Demo Area C, College Station, TX",
        ),
        (
            "Demo Parcel D - South Brazos",
            "DEMO-PARCEL-D",
            0.006,
            0.008,
            350,
            350,
            30,
            "Demo Area D, Brazos County, TX",
        ),
        (
            "Demo Parcel E - West of Bryan",
            "DEMO-PARCEL-E",
            -0.012,
            0.001,
            300,
            300,
            0,
            "Demo Area E, Bryan, TX",
        ),
    ]

    created: list[Parcel] = []
    for display_name, source_id, lon_off, lat_off, w, h, rot, address in parcels_data:
        lon = BRYAN_CS_LON + lon_off
        lat = BRYAN_CS_LAT + lat_off

        analysis_geom = make_parcel_polygon(lon, lat, w, h, rot)
        wgs84_geom = _to_wgs84(analysis_geom)
        centroid_wgs84 = Point(wgs84_geom.centroid.x, wgs84_geom.centroid.y)

        area_acres = sqm_to_acres(area_sqm(analysis_geom))

        parcel = Parcel(
            id=uuid.uuid4(),
            source_id=source_id,
            county_name="Brazos",
            display_name=display_name,
            address=address,
            source_area_acres=round(area_acres, 4),
            properties={
                "synthetic": True,
                "demo": True,
                "note": DEMO_NOTE,
            },
        )
        # Use raw SQL for geometry insertion to ensure proper SRID
        session.execute(
            text("""
                INSERT INTO parcels
                    (id, source_id, county_name, display_name, address,
                     source_area_acres, geometry, geometry_wgs84, centroid_wgs84,
                     properties, created_at, updated_at)
                VALUES
                    (:id, :source_id, :county_name, :display_name, :address,
                     :source_area_acres,
                     ST_GeomFromEWKT(:geom),
                     ST_GeomFromEWKT(:geom_wgs84),
                     ST_GeomFromEWKT(:centroid),
                     :properties, now(), now())
            """),
            {
                "id": str(parcel.id),
                "source_id": parcel.source_id,
                "county_name": parcel.county_name,
                "display_name": parcel.display_name,
                "address": parcel.address,
                "source_area_acres": float(area_acres),
                "geom": to_wkt_with_srid(analysis_geom, 32614),
                "geom_wgs84": to_wkt_with_srid(wgs84_geom, 4326),
                "centroid": to_wkt_with_srid(centroid_wgs84, 4326),
                "properties": '{"synthetic": true, "demo": true, "note": "%s"}' % DEMO_NOTE,  # noqa: UP031
            },
        )
        created.append(parcel)
        print(f"  Created parcel: {display_name} ({area_acres:.2f} acres)")

    return created


def _to_wgs84(geom):
    from app.geometry.crs import to_wgs84

    return to_wgs84(geom)


