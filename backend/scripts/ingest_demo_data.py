"""Ingest synthetic demo data for Brazos County, TX.

This script creates **clearly-labeled synthetic** demo data — it is NOT real
wetland, floodplain, or transmission line data. It exists to exercise the
full analysis pipeline end-to-end without requiring access to authoritative
datasets.

Usage:
    python -m scripts.ingest_demo_data
    # or
    python scripts/ingest_demo_data.py

Requires a PostGIS-enabled PostgreSQL database configured via DATABASE_URL.
"""

