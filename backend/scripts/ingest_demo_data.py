def ingest_constraints(session: Session, datasets: dict[str, DatasetMetadata]) -> None:
    """Create synthetic constraint features overlapping some parcels."""

    # Wetlands: 4 polygons, some overlapping parcels A, B, D
    wetlands = [
        # (lon_off, lat_off, width_m, height_m, classification)
        (-0.004, 0.004, 120, 100, None),  # near Parcel A
        (0.011, -0.003, 200, 150, None),  # near Parcel B
        (0.007, 0.009, 80, 80, None),  # near Parcel D
        (-0.003, -0.005, 60, 60, None),  # near Parcel C
    ]

    for i, (lon_off, lat_off, w, h, cls) in enumerate(wetlands):
        geom = make_constraint_polygon(BRYAN_CS_LON + lon_off, BRYAN_CS_LAT + lat_off, w, h)
        session.execute(
            text("""
                INSERT INTO constraint_features
                    (id, dataset_id, layer_type, source_id, classification,
                     geometry, properties, created_at)
                VALUES
                    (:id, :dataset_id, 'wetlands', :source_id, :classification,
                     ST_GeomFromEWKT(:geom), :properties, now())
            """),
            {
                "id": str(uuid.uuid4()),
                "dataset_id": str(datasets["wetlands"].id),
                "source_id": f"DEMO-WETLAND-{i + 1}",
                "classification": cls,
                "geom": to_wkt_with_srid(geom, 32614),
                "properties": '{"synthetic": true, "demo": true}',
            },
        )
    print(f"  Created {len(wetlands)} wetland features")

    # Floodplain: 3 polygons with FEMA-style classifications
    floodplains = [
        # (lon_off, lat_off, width_m, height_m, classification)
        (0.010, -0.004, 500, 400, "AE"),  # near Parcel B
        (0.006, 0.008, 300, 200, "A"),  # near Parcel D
        (-0.011, 0.002, 250, 180, "X"),  # near Parcel E
    ]

    for i, (lon_off, lat_off, w, h, cls) in enumerate(floodplains):
        geom = make_constraint_polygon(BRYAN_CS_LON + lon_off, BRYAN_CS_LAT + lat_off, w, h)
        session.execute(
            text("""
                INSERT INTO constraint_features
                    (id, dataset_id, layer_type, source_id, classification,
                     geometry, properties, created_at)
                VALUES
                    (:id, :dataset_id, 'floodplain', :source_id, :classification,
                     ST_GeomFromEWKT(:geom), :properties, now())
            """),
            {
                "id": str(uuid.uuid4()),
                "dataset_id": str(datasets["floodplain"].id),
                "source_id": f"DEMO-FLOOD-{i + 1}",
                "classification": cls,
                "geom": to_wkt_with_srid(geom, 32614),
                "properties": '{"synthetic": true, "demo": true}',
            },
        )
    print(f"  Created {len(floodplains)} floodplain features")

    # Transmission: 2 already-buffered corridor polygons
    transmissions = [
        # (lon_off, lat_off, width_m, height_m) — these represent already-buffered corridors
        (-0.008, 0.000, 60, 800),  # vertical corridor near Parcel E
        (0.003, 0.005, 600, 50),  # horizontal corridor near Parcel A/D
    ]

    for i, (lon_off, lat_off, w, h) in enumerate(transmissions):
        geom = make_constraint_polygon(BRYAN_CS_LON + lon_off, BRYAN_CS_LAT + lat_off, w, h)
        session.execute(
            text("""
                INSERT INTO constraint_features
                    (id, dataset_id, layer_type, source_id, classification,
                     geometry, properties, created_at)
                VALUES
                    (:id, :dataset_id, 'transmission', :source_id, NULL,
                     ST_GeomFromEWKT(:geom), :properties, now())
            """),
            {
                "id": str(uuid.uuid4()),
                "dataset_id": str(datasets["transmission"].id),
                "source_id": f"DEMO-TRANS-{i + 1}",
                "geom": to_wkt_with_srid(geom, 32614),
                "properties": '{"synthetic": true, "demo": true, "pre_buffered": true}',
            },
        )
    print(f"  Created {len(transmissions)} transmission features")


def main() -> None:
    settings = get_settings()
    print(f"Connecting to database: {settings.DATABASE_URL}")
    engine = create_engine(settings.DATABASE_URL, pool_pre_ping=True, future=True)

    # Create tables if they don't exist (idempotent for demo purposes)
    print("Creating tables if needed...")
    Base.metadata.create_all(engine)

    with Session(engine) as session:
        # Clear existing demo data (idempotent re-run)
        print("Clearing existing demo data...")
        session.execute(text("DELETE FROM constraint_features"))
        session.execute(text("DELETE FROM dataset_metadata"))
        session.execute(text("DELETE FROM parcels"))
        session.commit()

        print("Ingesting synthetic parcels...")
        ingest_parcels(session)
        session.commit()

        print("Ingesting dataset metadata...")
        datasets = ingest_datasets(session)
        session.commit()

        print("Ingesting constraint features...")
        ingest_constraints(session, datasets)
        session.commit()

    print("\nDemo data ingestion complete!")
    print("All data is SYNTHETIC and clearly labeled as demo.")


if __name__ == "__main__":
    main()
